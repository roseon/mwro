/**
 * A monster the player can fight against.
 */
export type PetTemplate = BaseMonsterTemplate & {
	build: BaseStats; // Not the final stats, will be multiplied to match level
	level: number;
	skills?: SkillsGroupJson[];
	petId?: number;
	resist?: Partial<Resist>;
};

/**
 * Called to get the PetTemplate of a pet by name, will error out if pet is not in petTemplates
 * @param key
 * @returns
 */
export function getPetTemplate(identifier: string | number): PetTemplate {
	let pet: PetTemplate | undefined;

	if (typeof identifier === 'string') {
		pet = petTemplates[identifier];
	} else if (typeof identifier === 'number') {
		pet = Object.values(petTemplates).find(p => p.petId === identifier);
	}

	if (!pet) {
		throw Error(`Pet ${identifier} does not exist in petTemplates`);
	}

	return pet;
}

